class ExampleOfBooleans {
  public static void main (String[] args) {
   Boolean x = false;
   if (x) {
     System.out.println("if(x) executed...");
   }
   if(!x) {
   System.out.println("if(!x) executed...");
   }
   if(x == false) {
     System.out.println("if(x == false) executed...");
   }
   System.exit(0);
  }
}
